export function getJuzInfo(ayahKey: string) {
  const [s, v] = ayahKey.split(":").map(Number);
  
  // Find current juz
  let juz = 1;
  for (const b of JUZ_BOUNDARIES) {
    if (s > b.s || (s === b.s && v >= b.v)) {
      juz = b.juz;
    } else {
      break;
    }
  }

  const surahName = SURAH_NAMES[s] || `Surah ${s}`;
  return { juz, surahName, label: `Juz ${juz} · ${surahName}` };
}
